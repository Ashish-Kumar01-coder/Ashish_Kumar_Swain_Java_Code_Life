package com.kodewala.practise.Office;

public class Employee {
  private String managerName;
  private String managerSalary;
  private String companyName;
  private String companyNeytWorth;
  public String getManagerName() {
	return managerName;
  }
  public String getManagerSalary() {
	return managerSalary;
  }
  public String getCompanyName() {
	return companyName;
  }
  public String getCompanyNeytWorth() {
	return companyNeytWorth;
  }
  public void setManagerName(String managerName) {
	this.managerName = managerName;
  }
  public void setManagerSalary(String managerSalary) {
	this.managerSalary = managerSalary;
  }
  public void setCompanyName(String companyName) {
	this.companyName = companyName;
  }
  public void setCompanyNeytWorth(String companyNeytWorth) {
	this.companyNeytWorth = companyNeytWorth;
  }
  
  
  
}
